    public void preencherProduto(){
        conex.conexao();
        conex.executaSql("select * from cadprodutos order by nome_produtos");
        try{
            conex.rs.first();
            jComboBoxProduto.removeAllItems();
            do{
                jComboBoxProduto.addItem(conex.rs.getString("nome_produtos"));
            }while(conex.rs.next());
        }catch(SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao buscar produto");            
        }
        conex.desconecta();   
    }
